    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        KhachHang kh = getFormData();

        if (kh == null) {
            return;
        }

        for (KhachHangResponse i : khachHangService.getAllViewTable()) {
            if (kh.getMa().equals(i.getMa())) {
                JOptionPane.showMessageDialog(this, "Mã khách hàng đã bị trùng");
                return;
            }
        }

        kh.setNgayTao(Date.valueOf(LocalDate.now()));

        if (khachHangService.add(kh)) {
            JOptionPane.showMessageDialog(this, "Thêm khách hàng thành công");
            List<KhachHangResponse> listNV = khachHangService.getAllViewTable();
            loadTable(list);
            clearForm();
        }    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int row = tblKH.getSelectedRow();
        KhachHang kh = khachHangService.getAll().get(row);

        KhachHang u = getFormData();
        kh.setMa(u.getMa());
        kh.setTen(u.getTen());

        kh.setSdt(u.getSdt());

        kh.setGioiTinh(u.getGioiTinh());
        kh.setDiemTichLuy(u.getDiemTichLuy());

        if (kh == null) {
            return;
        }

        kh.setNgayCapNhat(Date.valueOf(LocalDate.now()));

        if (khachHangService.update(kh)) {
            JOptionPane.showMessageDialog(this, "cập nhật thành công");
            List<KhachHangResponse> listNV = khachHangService.getAllViewTable();
            loadTable(list);
            clearForm();
        }     }//GEN-LAST:event_btnUpdateActionPerformed

