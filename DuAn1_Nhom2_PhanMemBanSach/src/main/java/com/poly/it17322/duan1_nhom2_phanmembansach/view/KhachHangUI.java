    private void loadTable(List<KhachHangResponse> list) {
        model.setRowCount(0);
        for (KhachHangResponse s : list) {
            Object[] rowData = {
                s.getMa(),
                s.getTen(),
                s.getGioiTinh(),
                s.getSdt(),
                s.getDiemTichLuy(),};
            model.addRow(rowData);
        }
    }

    private void clearForm() {
        this.txtMa.setText("");
        this.txtTenKH.setText("");
        this.txtSDT.setText("");
        txtDiemTichLuy.setText("");
    }

    private void showDetail(KhachHang kh) {
        txtMa.setText(kh.getMa());
        txtTenKH.setText(kh.getTen());
        txtSDT.setText(kh.getSdt());
//        txtDiemTichLuy.st(Integer.parseInt(kh.getDietTexemTichLuy()));

        if (kh.getGioiTinh() == 0) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }
    }

    private KhachHang getFormData() {
        String ma = txtMa.getText().trim();
        String ten = txtTenKH.getText().trim();
        int gioiTinh = rdoNam.isSelected() ? 0 : 1;
        String sdt = txtSDT.getText().trim();
        int diemTL = Integer.parseInt(txtDiemTichLuy.getText().trim());

        if (ma.length() == 0
                || ten.length() == 0
                || sdt.length() == 0) {
            JOptionPane.showMessageDialog(this, "Không được để trống");
            return null;
        }

        KhachHang kh = new KhachHang(null, ma, ten, null, sdt, gioiTinh, diemTL, null, null);
        return kh;

    }

    private void FillToTable(List<KhachHang> listKH) {
        model.setRowCount(0);
        for (KhachHang s : listKH) {
            Object[] rowData = {
                s.getMa(),
                s.getTen(),
                s.getGioiTinh(),
                s.getSdt(),
                s.getDiemTichLuy(),};
            model.addRow(rowData);
        }
    }

